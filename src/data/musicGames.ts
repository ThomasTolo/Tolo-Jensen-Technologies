export const songlessDays = uniqueDailyTracks(100, 0);

export const tuneGridDays = Array.from({ length: 100 }, (_, dayIndex) => uniqueTracksForDay(dayIndex, 10));

export function normalizeMusicAnswer(value: string) {
  return value.toUpperCase().replace(/[^A-Z0-9]/g, "");
}

function uniqueDailyTracks(days: number, offset: number) {
  return Array.from({ length: days }, (_, index) => musicTracks[(offset + index) % musicTracks.length]);
}

function uniqueTracksForDay(dayIndex: number, count: number) {
  const start = (100 + dayIndex * count) % musicTracks.length;
  const blockedTrack = songlessDays[dayIndex];
  const tracks: MusicTrack[] = [];

  for (let offset = 0; tracks.length < count; offset += 1) {
    const candidate = musicTracks[(start + offset) % musicTracks.length];

    if (
      candidate.title !== blockedTrack.title &&
      !tracks.some((trackItem) => trackItem.title === candidate.title && trackItem.artist === candidate.artist)
    ) {
      tracks.push(candidate);
    }
  }

  return tracks;
}
